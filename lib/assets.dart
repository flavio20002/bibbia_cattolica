String assetTextPrefazio(int number) {
  return 'assets/texts/liturgy/prefazi/prefazio${number.toString().padLeft(3, '0')}.md';
}

String assetTextDailyPrayer(String type, String dayOfTheWeek) {
  return 'assets/texts/preghiera_giorno/$type/$dayOfTheWeek.md';
}

String assetTextEveningPrayer(String type, String dayOfTheWeek) {
  return 'assets/texts/preghiera_sera/$type/$dayOfTheWeek.md';
}

String assetTextLecture(String url) {
  return 'assets/texts/liturgy/letture/${url.substring(1).replaceAll("/liturgia.yaml", "").replaceAll("/", "-").toLowerCase()}.txt';
}

String assetTextBible(String codCapitolo) {
  return 'assets/texts/bible/italian-cei2008/$codCapitolo.txt';
}

Future<String> loadTextFromAsset(String assetName) async {
  return (await rootBundle.loadString(assetName))
      .trim()
      .replaceAll(RegExp(r'\r'), '');
}
